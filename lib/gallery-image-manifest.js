const humanizeFilename = (file) =>
  file
    .replace(/\.[^.]+$/, "")
    .split("-")
    .map((part) => part.charAt(0).toUpperCase() + part.slice(1))
    .join(" ");

const buildGalleryImage = (file, overrides = {}) => ({
  src: `/client/gallery/${file}`,
  title: overrides.title ?? humanizeFilename(file),
  alt: overrides.alt ?? `${humanizeFilename(file)} project by Empire Fence`,
  category: overrides.category ?? "project",
  tags: overrides.tags ?? [],
  quality: overrides.quality ?? "standard",
  sourceName: overrides.sourceName ?? file,
});

